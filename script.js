

// Deklarasi variabel
var x = 10;
var y = 15;

// Pertambahan
var tambah = x + y;
document.write("Hasil Pertambahan: " + tambah,"<br>");

// Pengurangan
var kurang = x - y;
document.write("Hasil Pengurangan: " + kurang,"<br>");

// Perkalian
var kali = x * y;
document.write("Hasil Perkalian: " + kali,"<br>");

// Pembagian
var bagi = x / y;
document.write("Hasil Pembagian: " + bagi,"<br>");

// Modulus
var modulus = x % y;
document.write("Hasil Modulus: " + modulus,"<br>");
